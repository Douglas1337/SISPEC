package com.example.douglas.myapplication.Classes;

public class Animal{

    private int id;
    private int numBrinco;
    private int numRegistro;
    private String raca;
    private String pelagem;
    private String tipo;
    private String origemPai;
    private String origemMae;
    private Propriedade propriedade;
    private Abate abate;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getNumBrinco() {
        return numBrinco;
    }

    public void setNumBrinco(int numBrinco) {
        this.numBrinco = numBrinco;
    }

    public int getNumRegistro() {
        return numRegistro;
    }

    public void setNumRegistro(int numRegistro) {
        this.numRegistro = numRegistro;
    }

    public String getRaca() {
        return raca;
    }

    public void setRaca(String raca) {
        this.raca = raca;
    }

    public String getPelagem() {
        return pelagem;
    }

    public void setPelagem(String pelagem) {
        this.pelagem = pelagem;
    }

    public String getTipo() {
        return tipo;
    }

    public void setTipo(String tipo) {
        this.tipo = tipo;
    }

    public String getOrigemPai() {
        return origemPai;
    }

    public void setOrigemPai(String origemPai) {
        this.origemPai = origemPai;
    }

    public String getOrigemMae() {
        return origemMae;
    }

    public void setOrigemMae(String origemMae) {
        this.origemMae = origemMae;
    }

    public Propriedade getPropriedade() {
        return propriedade;
    }

    public void setPropriedade(Propriedade propriedade) {
        this.propriedade = propriedade;
    }

    public Abate getAbate() {
        return abate;
    }

    public void setAbate(Abate abate) {
        this.abate = abate;
    }



}
